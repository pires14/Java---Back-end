public class Aula08_LacoFor {
    public static void main(String[] args) {
        // Laço controlado com início, fim e passo definidos
        System.out.println("Tabuada do 5:");
        for (int i = 1; i <= 10; i++) {
            System.out.println("5 x " + i + " = " + (5 * i));
        }
    }
}